//loops are used to repeat a block of code multiple tim/


//there are three types of loops in javascript
//1. for loop
//2. while loop
//3. do while loop



//for loop
for (let i=0;i<5;i++){
    console.log(i);
}
console.log("for loop ends here");

//while loop
let i=0;
while(i<5){
    console.log(i);
    i++;
}
console.log("while loop ends here");

//do while loop
let j=0;

do{
    console.log(j);
    j++;    
}
while(j<5); 
console.log("do while loop ends here");

// the loop will continue after some time

